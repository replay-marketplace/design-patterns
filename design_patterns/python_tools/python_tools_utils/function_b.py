"""
Function B: Multiply two numbers
"""


def multiply_numbers(a: float, b: float) -> float:
    """
    Multiply two numbers together.
    
    Args:
        a: First number
        b: Second number
    
    Returns:
        The product of a and b
    
    Example:
        >>> multiply_numbers(2, 3)
        6
    """
    return a * b



"""
Function A: Add two numbers
"""


def add_numbers(a: float, b: float) -> float:
    """
    Add two numbers together.
    
    Args:
        a: First number
        b: Second number
    
    Returns:
        The sum of a and b
    
    Example:
        >>> add_numbers(2, 3)
        5
    """
    return a + b



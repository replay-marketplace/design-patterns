# Greeting functions module

def hello() -> str:
    """Return a Hello, World! greeting message.
    
    Returns:
        str: The greeting message "Hello, World!"
    """
    return "Hello, World!"


def goodbye() -> str:
    """Return a Goodbye! farewell message.
    
    Returns:
        str: The farewell message "Goodbye!"
    """
    return "Goodbye!"


def see_ya_later() -> str:
    """Return a See ya later! farewell message.
    
    Returns:
        str: The farewell message "See ya later!"
    """
    return "See ya later!"
